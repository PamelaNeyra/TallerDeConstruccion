package com.pe.sercosta.scks.models;



public class LoteOtModel {

	private String idLote;
	private String idPresentacion;
	private double cantidadMuestreado;
	
	public LoteOtModel() {
	}

	public String getIdLote() {
		return idLote;
	}

	public void setIdLote(String idLote) {
		this.idLote = idLote;
	}

	public String getIdPresentacion() {
		return idPresentacion;
	}

	public void setIdPresentacion(String idPresentacion) {
		this.idPresentacion = idPresentacion;
	}

	public double getCantidadMuestreado() {
		return cantidadMuestreado;
	}

	public void setCantidadMuestreado(double cantidadMuestreado) {
		this.cantidadMuestreado = cantidadMuestreado;
	}

	
	
}
