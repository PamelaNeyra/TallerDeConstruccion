package com.pe.sercosta.scks.models;

public class ProductoTerminadoSaldoModel {
	private String descripcion;
	private double cantidad_total;
	
	public ProductoTerminadoSaldoModel() {
		
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public double getCantidad_total() {
		return cantidad_total;
	}

	public void setCantidad_total(double cantidad_total) {
		this.cantidad_total = cantidad_total;
	}
	
	
}
